/// Holds snapshot / bar data for a product.
#[derive(Copy, Clone)]
pub struct Candle {
    open: f64,
    close: f64,
    high: f64,
    low: f64,
    volume: f64,
}

impl Open for Candle {
    fn open(&self) -> f64 {
        self.open
    }
}

impl Close for Candle {
    fn close(&self) -> f64 {
        self.close
    }
}

impl High for Candle {
    fn high(&self) -> f64 {
        self.high
    }
}

impl Low for Candle {
    fn low(&self) -> f64 {
        self.low
    }
}

impl Volume for Candle {
    fn volume(&self) -> f64 {
        self.volume
    }
}

